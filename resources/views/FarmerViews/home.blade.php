      <p class="card-text">Tomatoes love calcium and you probably have a ready supply that you’ve been throwing away. Rinse eggshells and leave them out to dry on a paper towel. Crush them up and add some to each hole when you are setting out your tomato seedlings in the garden. 

Blossom end rot is one of the biggest problems when growing tomatoes and there is nothing like the heartache of watching your almost ripe tomatoes begin to rot on the vine. Lack of calcium is the culprit. If you haven’t already added eggshells and blossom end rot begins to show up, water with some powdered milk and quickly remove all affected fruit. </p>
