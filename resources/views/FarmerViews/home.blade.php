      <h5 class="card-title">Plant Seedlings Deep</h5>
      <p class="card-text">When you’ve grown your tomatoes from seed and you are ready to plant those seedlings in the soil, make sure to remove some of the bottom leaves on the stem and plant them deep. Those little hairs on the stems actually want to be roots, and once in the soil, they will develop and create an even larger root network for your plant. 

Leave just a few stems at the top of the plant and bury as much stem as you can while keeping any leaves from touching the soil. Always remember to water new transplants really well for the first few days to help them get over the shock. 

